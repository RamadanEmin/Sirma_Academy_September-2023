function calculate(numOne, numTwo) {
    console.log(`Sum: ${numOne + numTwo}`);
    console.log(`Difference: ${Math.abs(numOne - numTwo)}`);
    console.log(`Product: ${numOne * numTwo}`);
    console.log(`Average: ${(numOne + numTwo) / 2}`);
}

calculate(25, 5);
calculate(7, 14);
calculate(12, 1);